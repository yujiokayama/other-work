  // 自要素のイベントを止める/実行する
  returnUndifindTset(val) {
    document.addEventListener('click', e => {
      // クリックされた要素のIDが「returnUndifind」だったら処理を抜ける
      if (!e.target.getAttribute('id') === 'returnUndifind') return;

      if (val === 'ng') {
        // 引数に「ng」が設定されたらaタグのイベントを防ぐ
        e.preventDefault();
        // return false;
      } else {
        return true;
      }
    });
  }

  /*
  //////////////////////////////////////////////////////////////
  伝播
  //////////////////////////////////////////////////////////////
  */
  propagationTset() {
    document.addEventListener('DOMContentLoaded', () => {
      document
        .querySelector('#parentClickArea')
        .addEventListener('click', () => {
          alert('親をクリック');
        });
      document.querySelector('#childClickArea').addEventListener('click', e => {
        alert('子をクリック');
        e.stopPropagation();
      });
    });
  }

